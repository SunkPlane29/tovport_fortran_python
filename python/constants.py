import numpy as np

si_c = 2.99792458e8
si_G = 6.67430e-11
si_MSOLAR = 1.98855e30
si_hbar = 6.582119569e-16

pi = np.pi

MASS_UNIT_TO_SI = si_MSOLAR
LENGTH_UNIT_TO_SI = si_G*si_MSOLAR/si_c**2
TIME_UNIT_TO_SI = si_G*si_MSOLAR/si_c**3
SI_TO_MASS_UNIT = 1.0/MASS_UNIT_TO_SI
SI_TO_LENGTH_UNIT = 1.0/LENGTH_UNIT_TO_SI
SI_TO_TIME_UNIT = 1.0/TIME_UNIT_TO_SI

PRESSURE_UNIT_TO_SI = MASS_UNIT_TO_SI*(LENGTH_UNIT_TO_SI**(-1))*(TIME_UNIT_TO_SI**(-2))
SI_TO_PRESSURE_UNIT = 1.0/PRESSURE_UNIT_TO_SI

hbarc = (si_c*1.0e15)*(si_hbar*1.0e-6)

FM4_TO_MEV4 = hbarc**4
MEV4_TO_FM4 = 1.0/FM4_TO_MEV4

FM4_TO_MEVFM3 = hbarc**3
MEVFM3_TO_FM4 = 1.0/FM4_TO_MEVFM3

MEV4_TO_MEVFM3 = hbarc**(-3)
MEVFM3_TO_MEV4 = 1.0/MEV4_TO_MEVFM3

EV_TO_JOULE = 1.602176634e-19
JOULE_TO_EV = 1.0/EV_TO_JOULE

MEV4_TO_JOULEM3 = ((si_hbar*1.0e-6)*si_c)**(-3) * EV_TO_JOULE*1.0e6
JOULE3_TO_MEV4 = 1.0/MEV4_TO_JOULEM3

MEVFM3_TO_PRESSURE_UNIT = MEVFM3_TO_MEV4 * MEV4_TO_JOULEM3 * SI_TO_PRESSURE_UNIT
PRESSURE_UNIT_TO_MEVFM3 = 1.0/MEVFM3_TO_PRESSURE_UNIT